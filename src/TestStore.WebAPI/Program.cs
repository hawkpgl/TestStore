using TestStore.Application;
using TestStore.CurrencyService;
using TestStore.EntityFramework;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddHttpClient();

ApplicationModule.ConfigureServices(builder.Services, builder.Configuration);
EntityFrameworkModule.ConfigureServices(builder.Services, builder.Configuration);
CurrencyServiceModule.ConfigureServices(builder.Services, builder.Configuration);

var app = builder.Build();

if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

// Configure the HTTP request pipeline.
app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();